    """Runtime error during engine processing.

    Raised when CUDA operations or engine processing fails.

    Parameters
    ----------
    message : str
        Error description.
    cuda_error : str, optional
        CUDA error message if available.

    Attributes
    ----------
    error_code : str
        Stable error identifier (E3020).
    cuda_error : str or None
        CUDA error message.
    hint : str or None
        Auto-generated based on error: "out of memory" suggests reducing
        parameters, "invalid configuration" suggests validation.
    context : dict
        Inherited from SigTekXError.
    """

    error_code = "E3020"

    def __init__(self, message: str, cuda_error: str | None = None):
        hint = None
        if cuda_error:
            if "out of memory" in cuda_error.lower():
                hint = "Reduce channels or nfft, or use a GPU with more memory"
            elif "invalid configuration" in cuda_error.lower():
                hint = "Check that nfft is a power of 2 and channels > 0"
        super().__init__(message, hint, cuda_error=cuda_error)
        self.cuda_error = cuda_error



"""
Custom exception hierarchy for sigtekx.

Enhanced with research-specific exceptions for benchmarking,
experiments, and reproducibility following RSE/RE standards.
"""

from typing import Any


class SigTekXError(Exception):
    """Base exception for all sigtekx errors.

    Provides consistent error handling with contextual hints and flexible
    context storage for debugging and error tracking.

    Parameters
    ----------
    message : str
        Human-readable error description.
    hint : str, optional
        Contextual hint for resolving the error. May be auto-generated by
        subclasses based on error parameters.
    **kwargs : dict
        Additional context stored in self.context. Common keys include field
        names, values, identifiers, state information, etc.

    Attributes
    ----------
    error_code : str
        Stable error identifier (E000).
    hint : str or None
        Resolution hint (explicit or auto-generated by subclasses).
    context : dict
        Additional error context from kwargs as key-value pairs.

    Examples
    --------
    >>> raise SigTekXError("Operation failed", hint="Check system logs")
    >>> raise SigTekXError("Config error", config_file="config.yaml", line=42)
    """

    error_code = "E000"

    def __init__(self, message: str, hint: str | None = None, **kwargs):
        super().__init__(message)
        self.hint = hint
        self.context = kwargs

    def __str__(self) -> str:
        msg = super().__str__()
        if self.hint:
            msg += f"\nHint: {self.hint}"
        if self.context:
            context_str = ", ".join(f"{k}={v}" for k, v in self.context.items())
            msg += f"\nContext: {context_str}"
        return msg

    def __repr__(self) -> str:
        """Machine-readable representation with error code."""
        return f"{self.__class__.__name__}({self.error_code!r}, {str(self)!r})"



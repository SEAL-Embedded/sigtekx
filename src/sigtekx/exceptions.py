class ResourceExhaustedError(SigTekXError):
    """System resource exhausted (memory, disk, etc.).

    Raised when system resources are insufficient for the requested operation.

    Parameters
    ----------
    resource_type : str
        Type of resource (e.g., 'gpu memory', 'disk space', 'file handles').
    required : Any, optional
        Amount of resource required.
    available : Any, optional
        Amount of resource available.

    Attributes
    ----------
    error_code : str
        Stable error identifier (E6020).
    resource_type : str
        Resource type that was exhausted.
    required : Any
        Required resource amount.
    available : Any
        Available resource amount.
    hint : str or None
        Auto-generated based on resource_type: 'gpu memory' suggests reducing
        parameters, 'disk space' suggests cleanup.
    context : dict
        Inherited from SigTekXError.
    """

    error_code = "E6020"

    def __init__(self, resource_type: str, required: Any = None, available: Any = None):
        message = f"{resource_type} exhausted"
        if required and available:
            message += f": required {required}, available {available}"

        hint = None
        if resource_type.lower() == "gpu memory":
            hint = "Reduce channels or nfft"
        elif resource_type.lower() == "disk space":
            hint = "Clean up old results or increase storage"

        super().__init__(message, hint=hint, resource_type=resource_type,
                        required=required, available=available)
        self.resource_type = resource_type
        self.required = required
        self.available = available

class ConfigError(SigTekXError):
    """Configuration validation or compatibility error.

    Raised when configuration parameters are invalid or incompatible.

    Parameters
    ----------
    message : str
        Error description.
    field : str, optional
        Name of the configuration field that failed.
    value : Any, optional
        Invalid value that was provided.
    hint : str, optional
        Custom hint. If None and field is provided, auto-generated as
        "Check the '{field}' field (current value: {value})".

    Attributes
    ----------
    error_code : str
        Stable error identifier (E1010).
    field : str or None
        Configuration field name.
    value : Any
        Invalid value provided.
    hint : str or None
        Resolution hint (explicit or auto-generated).
    context : dict
        Inherited from SigTekXError.
    """

    error_code = "E1010"

    def __init__(
        self,
        message: str,
        field: str | None = None,
        value: Any = None,
        hint: str | None = None,
    ):
        # If no explicit hint provided, build a simple field-based hint
        if hint is None and field:
            hint = f"Check the '{field}' field"
            if value is not None:
                hint += f" (current value: {value})"
        super().__init__(message, hint, field=field, value=value)
        self.field = field
        self.value = value



class DllLoadError(SigTekXError):
    """Failed to load required DLL/shared library.

    Raised when CUDA or engine library loading fails.

    Parameters
    ----------
    dll_name : str
        Name of the DLL/library that failed to load.
    original_error : Exception, optional
        Original exception from the loading attempt.

    Attributes
    ----------
    error_code : str
        Stable error identifier (E2020).
    dll_name : str
        Name of the failed DLL/library.
    original_error : Exception or None
        Original exception if available.
    hint : str
        Always set to "Check that CUDA toolkit is installed and on PATH".
    context : dict
        Inherited from SigTekXError.
    """

    error_code = "E2020"

    def __init__(self, dll_name: str, original_error: Exception | None = None):
        message = f"Failed to load {dll_name}"
        if original_error:
            message += f": {original_error}"
        hint = "Check that CUDA toolkit is installed and on PATH"
        super().__init__(message, hint, dll_name=dll_name, original_error=original_error)
        self.dll_name = dll_name
        self.original_error = original_error


class EngineStateError(SigTekXError):
    """Engine is in an invalid state for the requested operation.

    Raised when operations are attempted in incorrect engine states.

    Parameters
    ----------
    message : str
        Error description.
    current_state : str, optional
        Current engine state (e.g., "uninitialized", "processing").

    Attributes
    ----------
    error_code : str
        Stable error identifier (E3010).
    current_state : str or None
        Current engine state.
    hint : str or None
        Auto-generated based on state: "uninitialized" suggests calling
        initialize(), "processing" suggests waiting.
    context : dict
        Inherited from SigTekXError.
    """

    error_code = "E3010"

    def __init__(self, message: str, current_state: str | None = None):
        hint = None
        if current_state == "uninitialized":
            hint = "Call initialize() or use the Engine context manager"
        elif current_state == "processing":
            hint = "Wait for current operation to complete"
        super().__init__(message, hint, current_state=current_state)
        self.current_state = current_state



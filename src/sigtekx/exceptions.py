class EnvironmentMismatchError(ReproducibilityError):
    """Environment doesn't match expected configuration for reproduction.

    Raised when attempting to reproduce results in a different environment.

    Parameters
    ----------
    expected : dict[str, Any]
        Expected environment configuration.
    actual : dict[str, Any]
        Actual environment configuration.

    Attributes
    ----------
    error_code : str
        Stable error identifier (E4210).
    expected : dict[str, Any]
        Expected environment (passed to constructor).
    actual : dict[str, Any]
        Actual environment (passed to constructor).
    differences : list[str]
        Auto-calculated mismatches. Format: "key: expected=val1, actual=val2".
        Computed in __init__ by comparing expected vs actual dicts.
    missing_info : list[str] or None
        Inherited from ReproducibilityError (set to differences).
    hint : str or None
        Inherited from ReproducibilityError (auto-generated from differences).

    Examples
    --------
    >>> raise EnvironmentMismatchError(
    ...     {"cuda": "11.8", "driver": "525.60"},
    ...     {"cuda": "12.0", "driver": "525.60"}
    ... )
    """

    error_code = "E4210"

    def __init__(self, expected: dict[str, Any], actual: dict[str, Any]):
        differences = []
        for key in expected:
            if key in actual and expected[key] != actual[key]:
                differences.append(f"{key}: expected={expected[key]}, actual={actual[key]}")

        message = "Environment mismatch detected"
        if differences:
            message += f": {'; '.join(differences[:3])}"  # Show first 3 differences
            if len(differences) > 3:
                message += f" (and {len(differences)-3} more)"

        super().__init__(message, expected=expected, actual=actual, differences=differences)
        self.expected = expected
        self.actual = actual
        self.differences = differences


class DataIntegrityError(SigTekXError):
    """Data corruption or integrity check failure.

    Raised when data checksum or hash validation fails.

    Parameters
    ----------
    message : str
        Error description.
    expected_hash : str, optional
        Expected checksum/hash value.
    actual_hash : str, optional
        Actual checksum/hash value computed.

    Attributes
    ----------
    error_code : str
        Stable error identifier (E4220).
    expected_hash : str or None
        Expected hash value.
    actual_hash : str or None
        Actual hash value.
    hint : str
        Always starts with "Data may be corrupted or modified". If hashes
        provided, appends truncated hash comparison (first 8 chars).
    context : dict
        Inherited from SigTekXError.

    Examples
    --------
    >>> raise DataIntegrityError(
    ...     "Benchmark results file corrupted",
    ...     expected_hash="a1b2c3d4e5f6...",
    ...     actual_hash="fedcba987654..."
    ... )
    """

    error_code = "E4220"

    def __init__(self, message: str, expected_hash: str | None = None, actual_hash: str | None = None):
        hint = "Data may be corrupted or modified"
        if expected_hash and actual_hash:
            hint += f" (hash mismatch: expected {expected_hash[:8]}..., got {actual_hash[:8]}...)"
        super().__init__(message, hint=hint, expected_hash=expected_hash, actual_hash=actual_hash)
        self.expected_hash = expected_hash
        self.actual_hash = actual_hash



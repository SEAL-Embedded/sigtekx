"""
Ionosense HPC Benchmarking Dashboard
=====================================

Interactive dashboard for exploring GPU STFT benchmark results.

Launch with:
    streamlit run experiments/streamlit/app.py

Navigate between pages using the sidebar.
"""

import sys
from pathlib import Path

import streamlit as st

# Add parent directory to path for imports from experiments.analysis
sys.path.insert(0, str(Path(__file__).parent.parent))

# Import from our local utils (not streamlit package utils)
from utils.data_loader import get_data_freshness, load_benchmark_data

# Page configuration
st.set_page_config(
    page_title="Ionosense HPC Dashboard",
    page_icon="🚀",
    layout="wide",
    initial_sidebar_state="expanded",
)

# Main page
st.title("🚀 Ionosense HPC Benchmarking Dashboard")

st.markdown("""
Welcome to the **Ionosense HPC interactive dashboard**. This tool provides
real-time exploration of GPU STFT benchmark results for VLF/ULF ionosphere research.

### 📊 Available Pages

- **🏆 General Performance**: Overall throughput, latency, accuracy, and scaling analysis
- **🔬 Ionosphere Research**: Scientific metrics, phenomena detection, real-time factor analysis
- **⚙️ Configuration Explorer**: Interactive parameter filtering and comparison

### 🔄 Data Source

Dashboard reads data from `artifacts/data/` directory (generated by Snakemake workflow).
""")

# Data loading and status
st.divider()

col1, col2, col3 = st.columns(3)

try:
    with st.spinner("Loading benchmark data..."):
        data = load_benchmark_data("artifacts/data")

    col1.metric("Total Configurations", len(data))

    if 'benchmark_type' in data.columns:
        benchmark_types = data['benchmark_type'].nunique()
        col2.metric("Benchmark Types", benchmark_types)

    freshness = get_data_freshness("artifacts/data")
    if freshness:
        col3.metric("Last Updated", freshness)

    # Data preview
    st.subheader("📋 Data Preview")

    with st.expander("Show raw data (first 100 rows)", expanded=False):
        st.dataframe(
            data.head(100),
            use_container_width=True,
            hide_index=True,
        )

    # Quick stats
    st.subheader("📈 Quick Statistics")

    col1, col2, col3, col4 = st.columns(4)

    if 'frames_per_second' in data.columns:
        col1.metric(
            "Peak Throughput",
            f"{data['frames_per_second'].max():.1f} FPS",
            help="Maximum frames per second across all configurations"
        )

    if 'mean_latency_us' in data.columns:
        col2.metric(
            "Lowest Latency",
            f"{data['mean_latency_us'].min():.1f} μs",
            help="Minimum mean latency across all configurations"
        )

    if 'pass_rate' in data.columns:
        col3.metric(
            "Mean Accuracy",
            f"{data['pass_rate'].mean() * 100:.1f}%",
            help="Average pass rate across all accuracy tests"
        )

    if 'rtf' in data.columns:
        col4.metric(
            "Peak RTF",
            f"{data['rtf'].max():.2f}x",
            help="Maximum real-time factor (processing speed / data rate)"
        )

    # Navigation guidance
    st.divider()
    st.info(
        "👈 **Navigate using the sidebar** to explore detailed performance reports "
        "and interactive analysis tools."
    )

except FileNotFoundError:
    st.error(
        """
        ⚠️ **No benchmark data found**

        Please run benchmarks first:
        ```bash
        snakemake --cores 4 --snakefile experiments/Snakefile
        ```

        Data should be generated in `artifacts/data/` directory.
        """
    )
except Exception as e:
    st.error(f"❌ Error loading data: {e}")
    st.exception(e)

# Footer
st.divider()
st.caption(
    "ionosense-hpc-lib • GPU-accelerated STFT for VLF/ULF ionosphere research"
)
